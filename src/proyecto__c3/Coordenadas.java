public class Coordenadas {//clase contenedora de los atributos usados
    private int x,y;
    public Coordenadas(int x1, int y1){
        this.x=x1;
        this.y=y1;
    }

    //inicio de metoodos de encapsulamiento
    public int getX() {
        return x;
    }


    public void setX(int x) {
        this.x = x;
    }

  
    public int getY() {
        return y;
    }

  
    public void setY(int y) {
        this.y = y;
    }
    
}
